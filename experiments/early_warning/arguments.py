#!/usr/bin/env python
# -*- coding: utf-8 -*-
"""
TCR-GIN/experiments/early_warning/arguments.py

Argument parser for the early-warning training pipeline.
"""

import os
import yaml
import argparse
from typing import Dict, Any
from pathlib import Path


def parse_args():
    parser = argparse.ArgumentParser(
        description="Argument parser for PISS-based graph regression training."
    )

    # --- Core paths and experiment control ---
    parser.add_argument(
        '--config',
        type=str,
        default=None,
        help='Optional path to a config file. Command-line arguments override values from the config file.'
    )
    parser.add_argument(
        '--experiment_name',
        type=str,
        help='Unique name of the current experiment (usually generated by the launcher).'
    )
    parser.add_argument('--seed', type=int, help='Random seed.')
    parser.add_argument('--device', type=str, help='Device to use: cuda or cpu.')
    parser.add_argument('--num_runs', type=int, help='Number of repeated runs.')
    parser.add_argument(
        '--use_tqdm',
        type=lambda x: str(x).lower() == 'true',
        help='Whether to enable the tqdm progress bar.'
    )

    # --- Dataset parameters ---
    parser.add_argument('--train_path', type=str, help='Path to the training set.')
    parser.add_argument('--val_path', type=str, help='Path to the validation set.')
    parser.add_argument('--test_path', type=str, help='Path to the test set.')
    parser.add_argument('--cache_path', type=str, help='Path to the dataset cache.')
    parser.add_argument(
        '--rebuild_cache',
        type=lambda x: str(x).lower() == 'true',
        help='Whether to rebuild the cache.'
    )

    # --- Model architecture parameters (ablation / sensitivity) ---
    parser.add_argument(
        '--input_dim',
        type=int,
        help='Input feature dimension (usually determined by the data loader, but can also be set manually).'
    )
    parser.add_argument(
        '--feature_dim',
        type=int,
        default=None,
        help='[Sensitivity] Use the first N feature dimensions. If None or -1, use all features.'
    )
    parser.add_argument('--hidden_dim', type=int, help='[Grid search] Hidden dimension size.')
    parser.add_argument('--num_layers', type=int, help='[Grid search] Number of GNN layers.')
    parser.add_argument('--dropout', type=float, help='[Grid search] Dropout ratio.')
    parser.add_argument(
        '--jk_type',
        type=str,
        help='[Ablation] JumpingKnowledge type: last, cat, max, or lstm.'
    )
    parser.add_argument(
        '--use_virtual_node',
        type=lambda x: str(x).lower() == 'true',
        help='[Ablation] Whether to use a virtual node.'
    )
    parser.add_argument(
        '--use_residual',
        type=lambda x: str(x).lower() == 'true',
        help='[Ablation] Whether to use residual connections.'
    )
    parser.add_argument(
        '--activation_fn',
        type=str,
        help='[Ablation] Activation function: relu or gelu.'
    )

    # --- Training parameters ---
    parser.add_argument('--epochs', type=int, help='Maximum number of training epochs.')
    parser.add_argument('--batch_size', type=int, help='Batch size.')
    parser.add_argument('--num_workers', type=int, help='Number of data loader worker processes.')
    parser.add_argument('--lr', type=float, help='[Grid search] Learning rate.')
    parser.add_argument('--l2_reg', type=float, help='L2 regularization weight.')
    parser.add_argument(
        '--use_lr_scheduler',
        type=lambda x: str(x).lower() == 'true',
        help='Whether to use a learning rate scheduler.'
    )
    parser.add_argument('--warmup_epochs', type=int, help='Number of warmup epochs.')
    parser.add_argument('--min_lr', type=float, help='Minimum learning rate.')
    parser.add_argument('--eval_steps', type=int, help='Evaluate every N epochs.')
    parser.add_argument(
        '--monitor_metric',
        type=str,
        help='Metric used for early stopping (mae or loss).'
    )
    parser.add_argument('--early_stop_patience', type=int, help='Early stopping patience.')

    # --- PISS framework parameters (ablation / sensitivity) ---
    parser.add_argument(
        '--consistency_lambda',
        type=float,
        help='[Sensitivity/Ablation] Weight of the consistency loss.'
    )
    parser.add_argument(
        '--piss_k',
        type=int,
        help='[Sensitivity] Number of node removals used to generate positive samples in PISS.'
    )
    parser.add_argument(
        '--label_scale_factor',
        type=float,
        default=100.0,
        help='Scaling factor applied to labels to avoid numerical underflow or to better match the loss scale.'
    )

    # --- Output directories ---
    parser.add_argument('--output_dir', type=str, help='Directory for logs and results.')
    parser.add_argument('--model_dir', type=str, help='Directory for saving model checkpoints.')

    # =========================================================================
    # --- Core fix ---
    # Use a more robust config-merging strategy
    # =========================================================================

    # 1. Parse once only to get the --config path
    temp_args, _ = parser.parse_known_args()

    # 2. If a config file is provided, load it as defaults
    defaults = {}
    if temp_args.config:
        print(f"[*] Loading base configuration from file: {temp_args.config}")
        defaults = load_and_flatten_config(temp_args.config)

    # 3. Apply the loaded config as parser defaults
    parser.set_defaults(**defaults)

    # 4. Parse again with the correct priority:
    # command line > config file > hard-coded defaults
    args = parser.parse_args()

    # Ensure experiment_name always exists
    if args.experiment_name is None:
        if args.config:
            args.experiment_name = Path(args.config).stem + "_manual_run"
        else:
            args.experiment_name = "manual_run_no_config"

    return args


def load_and_flatten_config(config_path: str) -> Dict[str, Any]:
    """Load a YAML config file and flatten it into a single-level dictionary."""
    if not config_path or not os.path.exists(config_path):
        raise FileNotFoundError(f"Config file does not exist: {config_path}")

    with open(config_path, 'r', encoding='utf-8') as f:
        config_data = yaml.safe_load(f)

    flat_config = {}

    # Flatten the 'base_config' section
    if 'base_config' in config_data and isinstance(config_data['base_config'], dict):
        for section, params in config_data['base_config'].items():
            if isinstance(params, dict):
                flat_config.update(params)
            else:
                # This case is uncommon, but handled for robustness
                flat_config[section] = params

    return flat_config
