def load_graph_from_npz(npz_path):
    """Load an undirected graph from an NPZ edge list."""
    data = np.load(npz_path)
    edges = data["edges"]
    G = nx.Graph()
    if edges.size > 0:
        G.add_edges_from(edges.tolist())
    G.remove_edges_from(nx.selfloop_edges(G))
    return G


def simulate_full_lcc_curve(G, mode="dc"):
    """
    Simulate static attacks (DC/BC) until LCC <= 1 node.

    Returns
    -------
    (steps, lcc_fractions)
        step 0 corresponds to the intact graph.
    """
    N = G.number_of_nodes()
    if N == 0:
        return [0], [0.0]

    G_sim = G.copy()
    if mode == "dc":
        deg = dict(G.degree())
        order = sorted(G.nodes(), key=lambda x: (-deg[x], x))
    elif mode == "bc":
        bc = nx.betweenness_centrality(G)
        order = sorted(G.nodes(), key=lambda x: (-bc[x], x))
    else:
        raise ValueError(f"Unknown mode: {mode}")

    steps = [0]
    lcc_frac = [1.0]
    step_cnt = 0

    for node in order:
        if not G_sim.has_node(node):
            continue
        G_sim.remove_node(node)
        step_cnt += 1
        if G_sim.number_of_nodes() == 0:
            steps.append(step_cnt)
            lcc_frac.append(0.0)
            break
        lcc_size = len(max(nx.connected_components(G_sim), key=len))
        steps.append(step_cnt)
        lcc_frac.append(lcc_size / N)
        if lcc_size <= 1:
            break

    return steps, lcc_frac


def get_metric_series(df, net_name, tau_abs, algo, col):
    """
    Extract (step, value) series from the metrics table.

    Expected ID format in CSV:
        network = "<net_name>-<tau_abs>"
    """
    network_id = f"{net_name}-{tau_abs}"
    mask = (df["network"] == network_id) & (df["algorithm"] == algo)
    sub = df.loc[mask].sort_values("step").copy()
    if sub.empty:
        return np.array([]), np.array([])
    vals = pd.to_numeric(sub[col], errors="coerce")
    valid = ~vals.isna()
    return sub.loc[valid, "step"].values.astype(int), vals[valid].values


def tau_label(ratio):
    """Return a compact tau label in math mode, e.g., '$0.2N$'."""
    s = f"{ratio:g}"
    return rf"${s}N$"


def _legend_box(ax, handles, labels, loc="upper right", ncol=1, fontsize=10):
    """Draw a gray semi-transparent legend box."""
    leg = ax.legend(
        handles,
        labels,
        loc=loc,
        ncol=ncol,
        frameon=True,
        fancybox=True,
        framealpha=0.3,
        edgecolor="#cccccc",
        facecolor="#f0f0f0",
        fontsize=fontsize,
        handlelength=1.6,
        handletextpad=0.4,
        borderpad=0.3,
        labelspacing=0.3,
        columnspacing=0.8,
    )
    return leg


def setup_xaxis(ax, max_step, show_label=True):
    """Configure x-axis: minor tick every 1, major tick every 5."""
    ax.set_xlabel("Attack step" if show_label else "")
    ax.xaxis.set_major_locator(MultipleLocator(5))
    ax.xaxis.set_minor_locator(MultipleLocator(1))
    ax.set_xlim(0, max_step + 0.5)
    ax.tick_params(axis="x", which="major", length=6, direction="in")
    ax.tick_params(axis="x", which="minor", length=3, direction="in")


def compute_absolute_tau(N, ratio):
    """Convert ratio tau to absolute tau via round(ratio * N)."""
    return int(round(ratio * N))


def _pick_tau(setting, tau_low, tau_high):
    """Pick absolute tau from switch setting."""
    return tau_high if setting == "HIGH" else tau_low


def _pick_ratio(setting, ratio_low, ratio_high):
    """Pick ratio tau from switch setting."""
    return ratio_high if setting == "HIGH" else ratio_low


