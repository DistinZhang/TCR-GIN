    def _unify(axes, vmin, vmax, pad_frac=0.08):
        pad = max(0.005, (vmax - vmin) * pad_frac)
        lo = vmin - pad
        hi = vmax + pad
        for a in axes:
            a.set_ylim(lo, hi)

    _unify([ax_d, ax_h], min(nc_min1, nc_min2), max(nc_max1, nc_max2))
    _unify([ax_e, ax_i], min(r_min1, r_min2), max(r_max1, r_max2))
