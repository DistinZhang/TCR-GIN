def plot_nat_conn_subplot(ax, df, net_name, tau_low, tau_high, ratio_low, ratio_high, show_xlabel=True):
    """
    Natural connectivity panel with 4 curves:
      - DC at high tau
      - DC at low tau
      - BC at high tau
      - BC at low tau
    """
    tl_str = tau_label(ratio_low)
    th_str = tau_label(ratio_high)

    configs = [
        ("DC", tau_high, C_NAT_DC_HIGH, "--", 1.5, f"DC, {th_str}"),
        ("DC", tau_low, C_NAT_BC_LOW, "-", 1.3, f"DC, {tl_str}"),
        ("BC", tau_high, C_NAT_BC_HIGH, "--", 1.5, f"BC, {th_str}"),
        ("BC", tau_low, C_NAT_DC_LOW, "-", 1.3, f"BC, {tl_str}"),
    ]

    handles, labels, all_vals = [], [], []
    max_step = 0

    for algo, tau_abs, color, ls, lw, lbl in configs:
        s, v = get_metric_series(df, net_name, tau_abs, algo, "natural_connectivity")
        if len(s) == 0:
            print(f"  [WARN] Missing data: {net_name}-{tau_abs}, {algo}, natural_connectivity")
            continue

        z = 3 if ls == "--" else 1
        h, = ax.plot(s, v, color=color, ls=ls, lw=lw, zorder=z)
