def load_original_graph(net_name):
    """Load original graph for a network name."""
    p = DATA_DIR / net_name / f"{net_name}_edges.npz"
    if not p.exists():
        raise FileNotFoundError(f"Edge file not found: {p}")
    return load_graph_from_npz(str(p))


def load_attack_sequence(net_name, tau_abs, algo):
    """Load attack-removal sequence from attack result CSV."""
    tau_dir = DATA_DIR / net_name / f"{net_name}-{tau_abs}"
    csv_path = tau_dir / f"{net_name}-{tau_abs}-results.csv"
    if not csv_path.exists():
        return []

    df = pd.read_csv(csv_path)
    row = df[df["heuristic"] == algo]
    if row.empty:
        return []

    try:
        seq = ast.literal_eval(row.iloc[0]["removals"])
        return [int(x) for x in seq]
    except Exception:
        return []


def load_remnant_graph(net_name, tau_abs, algo, step):
    """Load remnant graph snapshot at a given step."""
    rem_dir = (
        DATA_DIR / net_name / f"{net_name}-{tau_abs}" /
        f"{net_name}-{tau_abs}-Remnants" / f"{net_name}-{tau_abs}-Remnants"
    )
    fname = f"{net_name}-{algo}_{step}_edges.npz"
    p = rem_dir / fname
    if not p.exists():
        print(f"  [WARN] Remnant file not found: {p}")
        return None
    return load_graph_from_npz(str(p))


