def draw_network_structure(ax, G, pos, title="", removed_nodes=None):
    """Draw one network snapshot with alive/removed nodes."""
    if removed_nodes is None:
        removed_nodes = set()

    alive_nodes = [n for n in G.nodes() if n not in removed_nodes]
    dead_nodes = [n for n in G.nodes() if n in removed_nodes]

    N = G.number_of_nodes()
    node_size_dead = max(5, min(30, 800 // max(N, 1)))
    node_size_alive = int(node_size_dead * 1.6)
    edge_width = max(0.1, min(0.5, 20.0 / max(N, 1)))

    alive_edges = [(u, v) for u, v in G.edges() if u not in removed_nodes and v not in removed_nodes]
    dead_edges = [(u, v) for u, v in G.edges() if u in removed_nodes or v in removed_nodes]

    # Removed edges
    if dead_edges:
        nx.draw_networkx_edges(
            G, pos, edgelist=dead_edges,
            edge_color=C_EDGE_DEAD, width=edge_width * 0.5,
            alpha=0.25, ax=ax
        )

    # Alive edges
    if alive_edges:
        ec = nx.draw_networkx_edges(
            G, pos, edgelist=alive_edges,
            edge_color="black", width=0.8, ax=ax
        )
        if ec is not None:
            ec.set_zorder(2)

    # Removed nodes
    if dead_nodes:
        nx.draw_networkx_nodes(
            G, pos, nodelist=dead_nodes,
            node_color=C_NODE_DEAD, node_size=node_size_dead,
            alpha=0.35, linewidths=0, ax=ax
        )

    # Alive nodes
    if alive_nodes:
        nc = nx.draw_networkx_nodes(
            G, pos, nodelist=alive_nodes,
            node_color="black", edgecolors="white",
            linewidths=1.2, node_size=node_size_alive, ax=ax
        )
        if nc is not None:
            nc.set_zorder(3)

    if title:
        ax.set_title(title, loc="left", fontweight="bold", fontsize=12, pad=2)
    ax.axis("off")


