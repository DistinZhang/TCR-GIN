def parse_args():
    parser = argparse.ArgumentParser(
        description="Create introduction figure comparing two networks.",
        formatter_class=argparse.RawTextHelpFormatter,
    )
    parser.add_argument("--net1", required=True, help="Name of network 1 (folder under data/)")
    parser.add_argument("--net2", required=True, help="Name of network 2 (folder under data/)")
    parser.add_argument("--tau_low", type=float, required=True, help="Low collapse target ratio (e.g., 0.2)")
    parser.add_argument("--tau_high", type=float, required=True, help="High collapse target ratio (e.g., 0.5)")
    parser.add_argument("--row4_steps", type=int, nargs=4, required=True, help="Four steps for row-4 snapshots")
    parser.add_argument("--row4_attack", type=str, default="DC", help="Attack mode for row-4 snapshots (DC/BC)")
    parser.add_argument("--input_csv", type=str, default="metrics.csv", help="Metrics CSV under results/")
    parser.add_argument("--seed", type=int, default=42, help="Random seed for spring layout")
    parser.add_argument(
        "--layout_k", type=float, default=None,
        help="k-scale for spring_layout (default effective scale=0.5; smaller -> tighter)"
    )
    parser.add_argument(
        "--opt_highlight_steps", type=int, nargs="*", default=[],
        help="Row-4 steps to highlight optimal removed set (e.g., 0 3 6 9)"
    )
    return parser.parse_args()


def main():
    args = parse_args()
    RESULTS_DIR.mkdir(parents=True, exist_ok=True)
    csv_path = RESULTS_DIR / args.input_csv

    # Load networks
    print(f"[INFO] Loading {args.net1} ...")
    G1 = load_original_graph(args.net1)
    N1 = G1.number_of_nodes()
    print(f"       N={N1}, E={G1.number_of_edges()}")

    print(f"[INFO] Loading {args.net2} ...")
    G2 = load_original_graph(args.net2)
    N2 = G2.number_of_nodes()
    print(f"       N={N2}, E={G2.number_of_edges()}")

    # Absolute taus
    tau_low_1 = compute_absolute_tau(N1, args.tau_low)
    tau_high_1 = compute_absolute_tau(N1, args.tau_high)
    tau_low_2 = compute_absolute_tau(N2, args.tau_low)
    tau_high_2 = compute_absolute_tau(N2, args.tau_high)
    print(f"[INFO] {args.net1}: tau_low={tau_low_1}, tau_high={tau_high_1}")
    print(f"[INFO] {args.net2}: tau_low={tau_low_2}, tau_high={tau_high_2}")

    # Load metrics CSV
    print(f"[INFO] Loading metrics table: {csv_path}")
    if not csv_path.exists():
        print(f"[ERROR] CSV not found: {csv_path}")
        raise SystemExit(1)

    df = pd.read_csv(csv_path)
    print(f"       {len(df)} rows")

    # Basic data availability check
    for net, tl, th in [(args.net1, tau_low_1, tau_high_1), (args.net2, tau_low_2, tau_high_2)]:
        for tau in [tl, th]:
            nid = f"{net}-{tau}"
            found_algos = df.loc[df["network"] == nid, "algorithm"].unique()
            if len(found_algos) == 0:
                print(f"  [WARN] No records for network='{nid}' in CSV")
            else:
                print(f"  [OK]   {nid}: algorithms={list(found_algos)}")

    # Simulate full LCC curves
    print("[INFO] Simulating full LCC curves ...")
    lcc_dc_1 = simulate_full_lcc_curve(G1, "dc")
    lcc_bc_1 = simulate_full_lcc_curve(G1, "bc")
    lcc_dc_2 = simulate_full_lcc_curve(G2, "dc")
    lcc_bc_2 = simulate_full_lcc_curve(G2, "bc")
    print(f"  {args.net1}: DC {len(lcc_dc_1[0])} steps, BC {len(lcc_bc_1[0])} steps")
    print(f"  {args.net2}: DC {len(lcc_dc_2[0])} steps, BC {len(lcc_bc_2[0])} steps")

    # Layout
    print("[INFO] Computing spring layouts ...")
    k_scale = args.layout_k if args.layout_k else 0.5
    k1 = k_scale / np.sqrt(N1)
    k2 = k_scale / np.sqrt(N2)
    pos1 = nx.spring_layout(G1, seed=args.seed, iterations=200, k=k1)
    pos2 = nx.spring_layout(G2, seed=args.seed, iterations=200, k=k2)

    # Draw and save figure
    print("[INFO] Rendering main figure ...")
    fig_main = create_main_figure(
        args, G1, G2, N1, N2,
        tau_low_1, tau_high_1, tau_low_2, tau_high_2,
        lcc_dc_1, lcc_bc_1, lcc_dc_2, lcc_bc_2,
        df, pos1, pos2
    )

    out_base = RESULTS_DIR / f"{args.net1}-{args.net2}"
    for fmt, kw in [(".pdf", {}), (".svg", {}), (".png", {"dpi": 600})]:
        fig_main.savefig(str(out_base) + fmt, bbox_inches="tight", **kw)
        print(f"  -> {out_base}{fmt}")
    plt.close(fig_main)

    print("\n[DONE] Figure generation completed.")


if __name__ == "__main__":
    main()
